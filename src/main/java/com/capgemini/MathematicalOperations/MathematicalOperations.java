package com.capgemini.MathematicalOperations;

/**
 * The MathematicalOperations implements four simple mathematical operations
 */
public class MathematicalOperations {
	/**
	 * @param number1
	 * @param number2
	 * @return sum of two numbers
	 */
	public int sum(int number1, int number2) {
		return number1 + number2;
	}
	
	/**
	 * @return product of <b>arg0</b> and <b>arg1</b>
	 */
	public double product(double arg0, double arg1) {
		return arg0*arg1;
	}
}
